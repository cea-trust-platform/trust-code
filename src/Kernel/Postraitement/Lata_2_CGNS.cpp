#include <Lata_2_Other.h>
#include <Lata_2_CGNS.h>
#include <EChaine.h>
#include <Nom.h>

Implemente_instanciable(Lata_2_CGNS, "Lata_To_CGNS|Lata_2_CGNS", Interprete);
// XD lata_to_CGNS interprete lata_to_CGNS -1 To convert results file written with LATA format to CGNS file. Warning: Fields located on faces are not supported yet.
// XD attr format format_lata_to_CGNS format 1 generated file post_CGNS.data use format (CGNS or LATA or LML keyword).
// XD attr file chaine file 0 LATA file to convert to the new format.
// XD attr file_CGNS chaine file_CGNS 0 Name of the CGNS file.

Sortie& Lata_2_CGNS::printOn(Sortie& os) const { return Interprete::printOn(os); }
Entree& Lata_2_CGNS::readOn(Entree& is) { return Interprete::readOn(is); }

Entree& Lata_2_CGNS::interpreter(Entree& is)
{
  Cerr << "Syntax Lata_To_CGNS::interpreter nom_lata||NOM_DU_CAS nom_fichier_sortie||NOM_DU_CAS   " << finl;
  Nom nom1, nom2;
  is >> nom1 >> nom2;

  if (nom2.finit_par(".cgns"))
    nom2.prefix(".cgns");

  Nom instruction("CGNS ");
  instruction += nom1;
  instruction += " ";
  instruction += nom2;

  Cerr << "command generated by Lata_2_CGNS " << instruction << finl;

  Lata_2_Other l2o;
  EChaine cmd(instruction);
  l2o.interpreter(cmd);

  return is;
}
